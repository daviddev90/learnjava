package com.javalec.var2;

public class Variable_02 {

	public static void main(String[] args) {
		
		char c = 'C';
		int i = 10;
		double d = 3.14;
		boolean b = false;
		String s = "대한민국";
		
		System.out.println(c); // 문자 C 출력
		System.out.println(i); // 정수 10 출력
		System.out.println(d); // 실수 3.14 출력
		System.out.println(b); // 부울 false 출력
		System.out.println(s); // 문자열 대한민국 출력

	}

}
